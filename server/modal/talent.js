const mongoose = require('mongoose')
const talentSchema = new mongoose.Schema({
    first_name: {
        type: String,
        required: true
    },
    last_name: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
   
}, { timestamps: true })
const talent = mongoose.model('talent', talentSchema)
module.exports = talent